    public void ListarDatos(){
        repuesto =db.getListadosRepuestos();
        DefaultTableModel tb =(DefaultTableModel)this.jTable1.getModel();
        for (Repuesto rp : repuesto){
            tb.addRow(new Object[]{
                rp.getID_repuesto(),
                rp.getNombre_rep(),
                rp.getDescripcion(),
                rp.getModelo_vehiculo(),
                rp.getMarca_vehiculo(),
                rp.getPrecio(),
                rp.getTipo_sistema(),
                rp.getMedidas(),
                rp.getMaterial(),
                rp.getCalidad(),
                rp.getGarantia(),
                rp.getID_Stock()           
            });
        }
    
    
    }   

